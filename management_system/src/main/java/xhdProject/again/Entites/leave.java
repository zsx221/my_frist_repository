package xhdProject.again.Entites;

public class leave {
	private int id ;
	private String name;
	private String department;
	private String post;
	private String application_time;
	private String type;
	private String frist_time;
	private String end_time;
	private String duration;
	private String context;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDepartment() {
		return department;
	}
	public void setDepartment(String department) {
		this.department = department;
	}
	public String getPost() {
		return post;
	}
	public void setPost(String post) {
		this.post = post;
	}
	public String getApplication_time() {
		return application_time;
	}
	public void setApplication_time(String application_time) {
		this.application_time = application_time;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getFrist_time() {
		return frist_time;
	}
	public void setFrist_time(String frist_time) {
		this.frist_time = frist_time;
	}
	public String getEnd_time() {
		return end_time;
	}
	public void setEnd_time(String end_time) {
		this.end_time = end_time;
	}
	public String getDuration() {
		return duration;
	}
	public void setDuration(String duration) {
		this.duration = duration;
	}
	public String getContext() {
		return context;
	}
	public void setContext(String context) {
		this.context = context;
	}
}
